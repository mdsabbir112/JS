function calculateSleepTime(times) {
  let sum = 0;
  for (let find of times) {
    if (typeof find !== "number") {
      return "Invalid";
    }
    sum += find;
  }

  let hours = 0;
  let minutes = 0;
  let seconds = 0;
  let x = 0;
  let y = 0;

  if (sum >= 3600) {
    hours = Math.floor(sum / 3600);
    x = hours * 3600;
    sum = sum - x;

    // minute
    minutes = Math.floor(sum / 60);
    y = minutes * 60;
    sum = sum - y;

    // secound
    seconds = sum;
  } 
  else if (sum < 3600) {
    // minute
    minutes = Math.floor(sum / 60);
    y = minutes * 60;
    sum = sum - y;

    // secound
    seconds = sum;
  } 
  else if (sum < 60) {
    seconds = sum;
  }
  return {
    hour: hours,
    minute: minutes,
    second: seconds,
  };
}

